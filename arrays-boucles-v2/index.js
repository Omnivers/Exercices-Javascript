// 01 - Nombre pairs
nombreMax=47;
nombreMin=11;
somme=0;
for (i=nombreMin;i<nombreMax;i++){
    if(i%2==0){
        console.log(`${i} Est un nombre pair`)
        somme=somme+i;
    }
}
console.log(somme);

